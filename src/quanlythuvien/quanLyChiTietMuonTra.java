    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int choice = JOptionPane.showOptionDialog(null, "Xác nhận xóa?", "Warning",

            JOptionPane.YES_NO_OPTION , JOptionPane.QUESTION_MESSAGE,

            null, null, null);
        if(choice==JOptionPane.YES_NO_OPTION){
            chiTietMuonTra ctmt = new chiTietMuonTra();
            ctmt.setMaMuonTra(jTextField1.getText());
            ctmt.setMaSach(jTextField2.getText());
            if(new connect().delChiTietMuonTra(ctmt)){
                JOptionPane.showMessageDialog(rootPane, "Đã xóa!");
                list.remove(ctmt);
                //int i = jTable1.getSelectedRow();
                //model.removeRow(i);
                clearTable();
                showTable();
            } else{
                JOptionPane.showMessageDialog(rootPane, "Mã mượn trả, mã sách không phù hợp!");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

