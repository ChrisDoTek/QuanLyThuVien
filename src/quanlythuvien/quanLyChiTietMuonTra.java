    public void showTable() { //xuất bảng
        list = new connect().getListChiTietMuonTra();
        for (chiTietMuonTra ctmt : list) {
            model.addRow(new Object[]{
            i++, ctmt.getMaMuonTra(), ctmt.getMaSach(), ctmt.getSoLuong(), ctmt.getNgayTra(), ctmt.getTienPhat()
            });
        }
    }
    
    public void clearTable() { //xóa bảng
            while(i!=1){
                i--;
                model.removeRow(i-1);
            }
    }
    
    public void showResult() {
        chiTietMuonTra ctmt = list.get(list.size() - 1);
        model.addRow(new Object[]{
            i++, ctmt.getMaMuonTra(), ctmt.getMaSach(), ctmt.getSoLuong(), ctmt.getNgayTra(), ctmt.getTienPhat()
        });
    }


