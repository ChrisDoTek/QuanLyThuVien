    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        chiTietMuonTra ctmt = new chiTietMuonTra();
        ctmt.setMaMuonTra(jTextField1.getText());
        ctmt.setMaSach(jTextField2.getText());
        ctmt.setSoLuong(Integer.parseInt(jTextField3.getText()));
        ctmt.setNgayTra(jTextField4.getText());
        if(jTextField5.getText().equals("")){
            ctmt.setTienPhat(0);
        }
        else
            ctmt.setTienPhat(Integer.parseInt(jTextField5.getText()));

        if(new connect().updateChiTietMuonTra(ctmt)){
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công!");
            clearTable();
            showTable();
        } else{
            JOptionPane.showMessageDialog(rootPane, "Lỗi! Xem lại mã mượn trả hoặc ngày mượn");
        }

    }//GEN-LAST:event_jButton4ActionPerformed
    
